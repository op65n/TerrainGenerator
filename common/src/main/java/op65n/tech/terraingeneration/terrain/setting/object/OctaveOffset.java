package op65n.tech.terraingeneration.terrain.setting.object;

public final class OctaveOffset {

    private final float x;
    private final float y;

    public OctaveOffset(final float x, final float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }

}
